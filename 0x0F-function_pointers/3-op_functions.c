#include "3-calc.h"

/**
 * op_add - add
 * @a: number
 * @b: number
 *
 * Return: result of adding two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract
 * @a: number
 * @b: number
 *
 * Return: result of subtracting b from a
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply
 * @a: number
 * @b: number
 *
 * Return: result of multiplying two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide
 * @a: number
 * @b: number
 *
 * Return: result of a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulo
 * @a: number
 * @b: number
 *
 * Return: result of a modulo b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
